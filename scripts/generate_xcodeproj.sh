#!/usr/bin/env bash
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "$0")/.." && pwd)"
cd "$ROOT_DIR"

if ! command -v xcodegen >/dev/null 2>&1; then
  echo "[ClassicLaunch] xcodegen이 필요합니다."
  echo "설치: brew install xcodegen"
  exit 1
fi

xcodegen generate --spec project.yml

echo "[ClassicLaunch] 생성 완료: $ROOT_DIR/ClassicLaunch.xcodeproj"
